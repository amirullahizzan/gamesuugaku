#include "Main.h"
#include "Game.h"
#include "Camera.h"

//---------------------------------------------------------------------------------
//	初期化処理
//---------------------------------------------------------------------------------
void Camera::Init()
{
	m_pos.set( 0.0f, 20.0f, -25.0f );
	m_look.set( 0.0f, 0.0f, -3.0f );
}
//---------------------------------------------------------------------------------
//	更新処理
//---------------------------------------------------------------------------------
void Camera::Update( Float3 player_pos )
{
	//	見る座標は受け取った値にします
	m_look.set( player_pos );
	//	カメラ座標は見る座標から
	m_pos.set( m_look );
	//	少し上
	m_pos.y += 15.0f;
	//	少し手前
	m_pos.z -= 15.0f;
}
//---------------------------------------------------------------------------------
//	描画処理
//---------------------------------------------------------------------------------
void Camera::Render()
{
	SetCameraPositionAndTarget_UpVecY( VGet( m_pos.x, m_pos.y, m_pos.z ), VGet( m_look.x, m_look.y, m_look.z ) );
}
//---------------------------------------------------------------------------------
//	終了処理
//---------------------------------------------------------------------------------
void Camera::Exit()
{
}
