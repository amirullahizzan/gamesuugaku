#include "Main.h"
#include "Base.h"

//---------------------------------------------------------------------------------
//	コンストラクタ
//---------------------------------------------------------------------------------
Base::Base()
{
	m_image = -1;
	m_pos.clear();
}
//---------------------------------------------------------------------------------
//	描画処理
//---------------------------------------------------------------------------------
void Base::Render(float camera_x)
{
	float screen_x = m_pos.x - camera_x;
	DrawGraphF( screen_x, m_pos.y, m_image, TRUE );
}
